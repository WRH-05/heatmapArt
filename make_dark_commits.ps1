        # Add different content each time
        Add-Content -Path $file -Value "<!-- commit $i for $d -->"
        
        # Set git environment variables for the commit date (with different times)
        $hour = 8 + ($i - 1)  # Spread commits across different hours
        $env:GIT_AUTHOR_DATE = "${d}T${hour}:00:00+00:00"
